// Flatten all Q&As with their category/subcategory metadata so the
// search box can do a single linear filter across everything.
export interface FlatFaqItem extends FaqQA {
  categoryId: string;
  categoryLabel: string;
  subcategoryTitle: string;
}

export function flattenFaq(): FlatFaqItem[] {
  return FAQ_CATEGORIES.flatMap((cat) =>
    cat.subcategories.flatMap((sub) =>
      sub.items.map((item) => ({
        ...item,
        categoryId: cat.id,
        categoryLabel: cat.label,
        subcategoryTitle: sub.title,
      })),
    ),
  );
}
